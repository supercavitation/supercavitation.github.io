  <a name="priority"></a>
  <h3 class="article-header">Priority Exclusve Moves</h3>
<div class="article-section">
	<p>These Pokemon are top contenders in their respective leagues and excellent Elite TM options. Many of them are exclusive Community Day moves, which may be obtained during special events throughout the year or during the December Community Day recap.</p>

	<h3>Swampert</h3>
	<p>
		<b>Leagues:</b> All<br>
		<b>Exclusive Charged Move:</b> Hydro Cannon
	</p>
	<p>Since Swampert has learned Hydro Cannon, it's been a contender in every league. Swampert has the most play in Great and Ultra League; Master League play is usually limited to special formats like Premier Classic. Hydro Cannon is an incredibly powerful Water-type move, and Swampert charges it with blinding speed thanks to Mud Shot. Swampert can compete in regular form or as a Shadow Pokemon; both are top picks and have their advantages and drawbacks.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Venusaur</h3>
	<p>
		<b>Leagues:</b> Great, Ultra<br>
		<b>Exclusive Charged Move:</b> Frenzy Plant
	</p>
	<p>Frenzy Plant is one of the strongest Charged Moves in the game and Venusaur benefits from fantastic Grass/Poison typing, which helps it reign over its fellow Grass types. Venusaur is a longtime meta staple, mostly in Great League, and pairs well with Pokemon such as Galarian Stunfisk or Bastiodon.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Walrein</h3>
	<p>
		<b>Leagues:</b> Great, Ultra, Master<br>
		<b>Exclusive Fast Move:</b> Powder Snow<br>
		<b>Exclusive Charged Move:</b> Icicle Spear
	</p>
	<p>Walrein crashed into the meta when it received its Community Day moves in January 2022. It has play in all three leagues, but is particularly dominant in the Ultra League's Premier Classic format. Walrein wants both Powder Snow and Icicle Spear to compete. It fast energy generation, low energy Ice type attacks, and powerful Earthquake coverage to make for strong neutral play against the meta.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Mewtwo</h3>
	<p>
		<b>Leagues:</b> Master<br>
		<b>Exclusive Charged Move:</b> Psystrike and/or Shadow Ball
	</p>
	<p>Mewtwo combines an immense Attack stat, fast energy generation, and a strong movepool to make for one of the most powerful and dynamic picks in Master League. Psystrike is mandatory as Mewtwo's lowest energy move. It can be paired with Mewtow's other exclusive move, Shadow Ball, or any of its coverage moves (usually Focus Blast or Ice Beam). Mewtwo can compete in regular form or as a Shadow Pokemon, although its regular form is more reliable in PvP. (<i>Never purify your Shadow Mewtwo!</i>)</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Metagross</h3>
	<p>
		<b>Leagues:</b> Master<br>
		<b>Exclusive Charged Move:</b> Meteor Mash
	</p>
	<p>Metagross serves an important role in Master League countering top threats such as Zacian and Dialga. In other formats such as Premier Classic, Metagross is a top threat in its own right. Meteor Mash is necessary for Metagross to compete, providing a lower energy Charged Move and excellent damage output.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Registeel</h3>
	<p>
		<b>Leagues:</b> Great, Ultra<br>
		<b>Exclusive Charged Move:</b> Zap Cannon<br>
	</p>
	<p>Registeel's supreme energy generation and bulk combine to make Zap Cannon terrifyingly effective, especially in open Ultra League. Zap Cannon is a hard-hitting Electric-type Charged Move that lowers the opponent's Attack, making Registeel even more difficult to KO. It also targets that could previously wall Registeel, such as Jellicent and Talonflame. Zap Cannon should be paired with Focus Blast to fend against common counters like Galarian Stunfisk.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Talonflame</h3>
	<p>
		<b>Leagues:</b> Great, Ultra<br>
		<b>Exclusive Fast Move:</b> Incinerate<br>
	</p>
	<p>Incinerate bolsters Talonflame's ability to boost itself with Flame Charge, and deliver crushing knock-out hits with Brave Bird. Talonflame is particularly dominant in Ultra League, where few Pokemon resist both of its attacks. Incinerate is the longest Fast Move in the game (5 turns), so if you're uncomfortable playing longer moves like Confusion or Volt Switch, it may not be the right Pokemon for you.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Ho-Oh</h3>
	<p>
		<b>Leagues:</b> Ultra, Master<br>
		<b>Exclusive Charged Move:</b> Earthquake or Sacred Fire
	</p>
	<p>Ho-Oh became a top pick in Master League once it gained access to Incinerate and its first exclusive move, Earthquake. Ho-Oh plays similarly to Talonflame and pairs Earthquake or Sacred Fire with the hard-hitting move Brave Bird. Earthquake generally has superior coverage but Sacred Fire is more powerful in neutral matchups and has a chance to debuff the opponent, swinging matchups that Earthquake can't. Ho-Oh is one of the most technically challenging Pokemon to play, so be prepared for a steep learning curve if you choose to train one.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Lugia</h3>
	<p>
		<b>Leagues:</b> Ultra, Master<br>
		<b>Exclusive Charged Move:</b> Aeroblast
	</p>
	<p>Lugia's exclusive move, Aeroblast, is one of the hardest hitting moves in the game, and Lugia backs it up with the highest stats of almost any Master League Pokemon. Lugia most often plays as a safe switch in Master League thanks to its generally neutral matchups and its ability to shrug off even super effective hits. Lugia can compete in regular form or as a Shadow Pokemon, although its regular form is more reliable in PvP.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Umbreon</h3>
	<p>
		<b>Leagues:</b> Great, Ultra<br>
		<b>Exclusive Charged Move:</b> Last Resort<br>
	</p>
	<p>Umbreon is an extremely bulky Pokemon, able to absorb tons of damage in both Great and Ultra League. Its exclusive move, Last Resort, isn't very powerful but helps Umbreon deliver neutral hits against Fairy types like Azumarill, or against other Dark types. Psychic is another exclusive option for Umbreon, but it generally struggles as a generalist without the neutral play that Last Resort provides.</p>

	<!---------------------------------------------------------------------------------------->

	<h3>Cresselia</h3>
	<p>
		<b>Leagues:</b> Great, Ultra<br>
		<b>Exclusive Charged Move:</b> Grass Knot<br>
	</p>
	<p>Cresselia is bulky Psychic type and a longtime meta staple in Ultra League. Grass Knot is its fastest move and gives it an upper hand against Water types such as Swampert and Walrein. Grass Knot is recommended for Great League Cresselia to hit Azumarill. Cresselia must be obtained from special research or traded to fall under 1500 CP.</p>

