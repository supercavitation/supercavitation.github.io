  <a name="other"></a>
  <h3 class="article-header">Other</h3>
<div class="article-section">

		<h3>Charizard</h3>
		<p>
			<b>Leagues:</b> Great, Ultra<br>
			<b>Exclusive Fast Move:</b> Wing Attack or Dragon Breath<br>
			<b>Exclusive Charged Move:</b> Blast Burn<br>
		</p>
		<p>Charizard's exclusive move, Blast Burn, is extremely powerful. As a Fire type, Charizard can struggle in open formats but has the most play in limited cups like Love Cup or Ultra Premier Classic. Blast Burn is mandatory to give Charizard its closing power, while exclusive Fast Moves such as Wing Attack or Dragon Breath are optional. Wing Attack provides Charizard with its fastest energy generation while Dragon Breath gives it more play against specific targets like Giratina or Dragonite.</p>

		<!---------------------------------------------------------------------------------------->

		<h3>Lickitung</h3>
		<p>
			<b>Leagues:</b> Great<br>
			<b>Exclusive Charged Move:</b> Body Slam<br>
		</p>
		<p>Lickitung rose to relevancy with the level 50 update. Its high bulk and Body Slam spam make it a safe pick in Great League that synergizes well with other meta staples, such as Medicham. An XL Lickitung is a costly investment, so if you're on the way to building one, Elite TM's are probably the least of your challenges!</p>

		<!---------------------------------------------------------------------------------------->

		<h3>Groudon</h3>
		<p>
			<b>Leagues:</b> Master<br>
			<b>Exclusive Charged Move:</b> Fire Punch<br>
		</p>
		<p>Groudon is a powerful option for Master League, and Fire Punch plays a critical role in its viability. Fire Punch is Groudon's cheapest move, which gives Groudon the opportunity to spam or bait for its bigger move, Earthquake. Groudon is viable with both the energy generation of Mud Shot or the power of Dragon Tail as generalist.</p>

		<!---------------------------------------------------------------------------------------->

		<h3>Zapdos</h3>
		<p>
			<b>Leagues:</b> Great<br>
			<b>Exclusive Fast Move:</b> Thunder Shock
		</p>
		<p>Zapdos is mostly shunned from open league play thanks to the overwhelming presence of Galarian Stunfisk, but it's a dynamic glass cannon that can be fun and rewarding to play. Zapdos has excellent defensive typing, and it needs all the help it can get because it can't take a hit. Its speed will surprise both you and your opponent, and it has powerful Charged Moves to boot in Drill Peck, Thunderbolt, or Thunder. Thunder Shock Zapdos is unique among other Pokemon of its types and can shine in the occassional themed cup.</p>

		<!---------------------------------------------------------------------------------------->

		<h3>Lapras</h3>
		<p>
			<b>Leagues:</b> Great, Ultra<br>
			<b>Exclusive Fast Move:</b> Ice Shard<br>
			<b>Exclusive Charged Move:</b> Ice Beam<br>
		</p>
		<p>Lapras is comparable to Walrein, and trades coverage for extra bulk and Water-flavored attacks. Ice Shard is mandatory in most formats, while Ice Beam is optional and usually dropped in favor of Skull Bash; Skull Bash increases Lapras's already high defenses and gives it neutral play against other Water types.</p>

		<!---------------------------------------------------------------------------------------->

		<h3>Dewgong</h3>
		<p>
			<b>Leagues:</b> Great<br>
			<b>Exclusive Fast Move:</b> Ice Shard<br>
			<b>Exclusive Charged Move:</b> Icy Wind<br>
		</p>
		<p>Double legacy Dewgong can frustrate your opponent with its bulk and Attack drops. Walrein is more popular, but Dewgong can still make a strong showing especially in themed cups. Ice Shard and Icy Wind are mandatory together to turn Dewgong into an oppressive debuff machine. The exclusive Charged Move Aqua Jet is not recommended, since Water Pulse is slightly better.</p>

		<!---------------------------------------------------------------------------------------->


		<h3>Seaking</h3>
		<p>
			<b>Leagues:</b> Great, Ultra<br>
			<b>Exclusive Fast Move:</b> Poison Jab<br>
			<b>Exclusive Charged Move:</b> Icy Wind and Drill Run<br>
		</p>
		<p>Triple legacy Seaking is a spice unicorn! While it doesn't offer much play in open leagues, it can find niches in special formats like Love Cup. Seaking packs off-typing moves to give it an interesting damage profile with debuff ability and wide coverage. It's slightly lacking in bulk and speed, however, so it may underperform compared to similar Pokemon like Dewgong.</p>

